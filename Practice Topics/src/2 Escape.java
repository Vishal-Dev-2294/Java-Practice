class Escape {
    public static void main(String[] args) {
        System.out.println("Hello \'Vishal\' Bhai");
        System.out.println("Hello \"Vishal\" Bhai");
        System.out.println("Hello\tVishal Bhai");
        System.out.println("Hello\tVishal Bhai");
        System.out.println("Hello\tVishal\nBhai");
        System.out.println("Hello\bVishal Bhai"); //apne cursor ko ek step piche le aata hai
        System.out.println("*\n*\t*\n*\t*\t*\n*\t*\t*\t*\n*\t*\t*\t*\t*\n");
    }
}
